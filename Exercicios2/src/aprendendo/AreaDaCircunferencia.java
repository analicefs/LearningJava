package aprendendo;

public class AreaDaCircunferencia {
	
		public static void main(String[] args) {
			double raio=3.0;
		final double PI =3.1459; // nome de constante com letra maiúscula
		double area= PI*raio*raio;
		
		System.out.println("Área=" + area+ "m2");// o + concatenar 
			raio=10;
			area= PI*raio*raio;
		System.out.println("Área=" + area+ "m2");
		}

	}


